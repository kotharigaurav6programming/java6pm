// program to add two numbers

class Demo2{
    public static void main(String args[]){
        int num1 = 5, num2 = 2, result;

        result = num1 + num2;
        System.out.println(num1+" + "+num2+" = "+result);

        result = num1 - num2;
        System.out.println(num1+" - "+num2+" = "+result);

        result = num1 * num2;
        System.out.println(num1+" * "+num2+" = "+result);

        result = num1 / num2;
        System.out.println(num1+" / "+num2+" = "+result);

        result = num1 % num2;
        System.out.println(num1+" % "+num2+" = "+result);

    }
}